export function validateProPlayer(player) {
  const hasBaseKeys = PRO_PLAYER_REQUIRED_KEYS.every((key) => Object.prototype.hasOwnProperty.call(player, key))
  const hasStats =
    player &&
    typeof player.stats === 'object' &&
    PRO_PLAYER_STATS_KEYS.every((key) => typeof player.stats[key] === 'number' && player.stats[key] >= 1 && player.stats[key] <= 20)

  const validRole = ['Top', 'Jungle', 'Mid', 'ADC', 'Support'].includes(player?.role)
  const validSignatures =
    Array.isArray(player?.signature_champions) &&
    player.signature_champions.length === 3 &&
    player.signature_champions.every((champion) => typeof champion === 'string' && champion.length > 0)

  return hasBaseKeys && hasStats && validRole && validSignatures && typeof player.value === 'number'
}

